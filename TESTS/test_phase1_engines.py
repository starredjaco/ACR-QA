class TestByoLlmKey:
    def _clean_env(self, monkeypatch):
        """Force groq provider and clear all key env vars."""
        monkeypatch.setenv("ACRQA_LLM_PROVIDER", "groq")
        for i in range(1, 5):
            monkeypatch.delenv(f"GROQ_API_KEY_{i}", raising=False)
        monkeypatch.delenv("GROQ_API_KEY", raising=False)
        monkeypatch.delenv("LLM_API_KEY", raising=False)
        monkeypatch.delenv("ACRQA_BYO_KEY", raising=False)

    def test_byo_key_via_env(self, monkeypatch):
        """LLM_API_KEY env var overrides GROQ_API_KEY_* vars."""
        self._clean_env(monkeypatch)
        monkeypatch.setenv("LLM_API_KEY", "test-byo-key-abc")

        from importlib import reload

        import CORE.engines.explainer as explainer_mod

        reload(explainer_mod)

        pool = explainer_mod.KeyPool()
        assert "test-byo-key-abc" in pool._keys

    def test_byo_key_via_constructor(self, monkeypatch):
        """Constructor byo_api_key takes priority over env vars."""
        self._clean_env(monkeypatch)

        from importlib import reload

        import CORE.engines.explainer as explainer_mod

        reload(explainer_mod)

        pool = explainer_mod.KeyPool(byo_api_key="constructor-byo-key")
        assert pool._keys == ["constructor-byo-key"]

    def test_byo_key_acrqa_env_var(self, monkeypatch):
        """ACRQA_BYO_KEY also works as the env var name."""
        self._clean_env(monkeypatch)
        monkeypatch.setenv("ACRQA_BYO_KEY", "acrqa-byo-key-xyz")

        from importlib import reload

        import CORE.engines.explainer as explainer_mod

        reload(explainer_mod)

        pool = explainer_mod.KeyPool()
        assert "acrqa-byo-key-xyz" in pool._keys


