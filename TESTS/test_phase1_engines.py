class TestAiCodeDiffFilter:
    def test_init_default(self, tmp_path):
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        assert af._lookback == 200

    def test_init_custom_lookback(self, tmp_path):
        af = AiCodeDiffFilter(repo_dir=tmp_path, lookback_commits=50)
        assert af._lookback == 50

    def test_get_ai_touched_files_no_git(self, tmp_path):
        # Non-git dir — should return empty set, not raise
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        result = af.get_ai_touched_files()
        assert isinstance(result, set)

    def test_filter_findings_empty_ai_files(self, tmp_path):
        """When no AI files detected, returns all findings with warning."""
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        findings = [{"file": "src/app.py", "message": "test"}]
        result = af.filter_findings(findings)
        # Should return all findings (fallback mode)
        assert len(result) == len(findings)

    def test_filter_findings_matches_ai_file(self, tmp_path):
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        af._ai_files = {"src/app.py"}  # force inject
        findings = [
            {"file": "src/app.py", "message": "vuln"},
            {"file": "src/utils.py", "message": "clean"},
        ]
        result = af.filter_findings(findings)
        assert len(result) == 1
        assert result[0]["file"] == "src/app.py"
        assert result[0]["ai_touched"] is True

    def test_filter_findings_file_path_field(self, tmp_path):
        """Handles file_path field (DB findings use this name)."""
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        af._ai_files = {"src/app.py"}
        findings = [{"file_path": "src/app.py", "message": "vuln"}]
        result = af.filter_findings(findings)
        assert len(result) == 1

    def test_filter_findings_empty_input(self, tmp_path):
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        af._ai_files = {"src/app.py"}
        assert af.filter_findings([]) == []

    def test_summary_structure(self, tmp_path):
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        af._ai_files = {"a.py", "b.py"}
        s = af.summary()
        assert "ai_touched_files" in s
        assert s["ai_touched_count"] == 2
        assert s["lookback_commits"] == 200

    def test_is_ai_commit_copilot(self):
        msg = "Add feature\n\nCo-authored-by: GitHub Copilot <copilot@github.com>"
        assert AiCodeDiffFilter._is_ai_commit(msg) is True

    def test_is_ai_commit_claude(self):
        msg = "fix: update auth\n\nCo-Authored-By: Claude Sonnet 4.6 <noreply@anthropic.com>"
        assert AiCodeDiffFilter._is_ai_commit(msg) is True

    def test_is_ai_commit_normal(self):
        msg = "fix: update auth logic\n\nReviewed-by: Alice"
        assert AiCodeDiffFilter._is_ai_commit(msg) is False

    def test_is_ai_commit_cursor(self):
        msg = "Generated by Cursor: implement search feature"
        assert AiCodeDiffFilter._is_ai_commit(msg) is True

    def test_header_marker_detection(self, tmp_path):
        py_file = tmp_path / "generated.py"
        py_file.write_text("# Generated by Copilot\ndef foo():\n    pass\n")
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        detected = af._files_with_header_markers()
        assert "generated.py" in detected

    def test_header_marker_not_detected_normal(self, tmp_path):
        py_file = tmp_path / "normal.py"
        py_file.write_text("def bar():\n    return 42\n")
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        detected = af._files_with_header_markers()
        assert "normal.py" not in detected

    def test_caching(self, tmp_path):
        af = AiCodeDiffFilter(repo_dir=tmp_path)
        r1 = af.get_ai_touched_files()
        r2 = af.get_ai_touched_files()
        assert r1 is r2  # same object — cached

    def test_is_source_extensions(self):
        assert AiCodeDiffFilter._is_source("app.py") is True
        assert AiCodeDiffFilter._is_source("server.go") is True
        assert AiCodeDiffFilter._is_source("index.ts") is True
        assert AiCodeDiffFilter._is_source("README.md") is False
        assert AiCodeDiffFilter._is_source("config.yml") is False

    def test_ai_commit_emoji_pattern(self):
        msg = "🤖 Generated by Claude — implement OAuth flow"
        assert AiCodeDiffFilter._is_ai_commit(msg) is True

    def test_ai_commit_gpt_pattern(self):
        msg = "Written using GPT-4 — initial scaffolding"
        assert AiCodeDiffFilter._is_ai_commit(msg) is True


