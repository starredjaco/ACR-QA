class TestConfirmedTierApiFilter:
    def test_confirmed_tier_filter_logic(self):
        """The CT filter in get_findings uses ConfirmedTierEngine correctly."""
        from CORE.engines.confirmed_tier import ConfirmedTierEngine

        findings = [
            {
                "canonical_severity": "high",
                "canonical_rule_id": "SECURITY-001",
                "file_path": "src/app.py",
                "tool_raw": {"tool_name": "semgrep"},
            },
            {
                "canonical_severity": "low",
                "canonical_rule_id": "IMPORT-001",
                "file_path": "src/app.py",
                "tool_raw": {"tool_name": "ruff"},
            },
        ]
        engine = ConfirmedTierEngine()
        confirmed = [f for f in findings if engine.classify({**f, "file": f.get("file_path", "")}).in_confirmed_tier]
        assert len(confirmed) == 1
        assert confirmed[0]["canonical_rule_id"] == "SECURITY-001"


