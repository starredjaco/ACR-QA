class TestConfirmedFixRateKpi:
    def test_fix_rate_calculation(self):
        """Fix rate = TP findings / all confirmed findings with feedback."""
        confirmed_tp = 7
        confirmed_total = 10
        fix_rate = round(confirmed_tp / confirmed_total * 100, 1)
        target_met = fix_rate >= 70.0
        assert fix_rate == 70.0
        assert target_met is True

    def test_fix_rate_below_target(self):
        fix_rate = round(4 / 10 * 100, 1)
        assert fix_rate < 70.0

    def test_fix_rate_no_feedback(self):
        total = 0
        rate = None if total == 0 else round(1 / total * 100, 1)
        assert rate is None
